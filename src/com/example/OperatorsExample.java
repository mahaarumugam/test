package com.example;

public class OperatorsExample {
public static void main(String []args)
{
	int count =20;
	count++;
	System.out.println(count>30);
	System.out.println(count<30);
	System.out.println(count>=30);
	System.out.println(count<=30);
	System.out.println(9 & 5);
	System.out.println(9 ^ 5);
	System.out.println(9 | 5);
	System.out.println(30+15-2*(4+5)+"Good"+50+11);
	
	

}

}
