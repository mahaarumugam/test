package com.example;

public class SwitchProgram {
	public static void main(String []args)
	{
		int x=2;
		switch (x){
		case 1:
			System.out.println(25);
			break;
		case 2:
			System.out.println(50);
			break;
		case 3:
			System.out.println(75);
			break;
default:
	System.out.println("default");
	break;
		}
	}

}
