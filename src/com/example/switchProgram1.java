package com.example;

public class switchProgram1 {
	public static void main(String a[])
	{
	int n = 2;
	switch (n)
	{
	case 1:
	System.out.println( "The number is 1" );
	break;
	case 2:
	System.out.println( "The number is 2" );
	break;
	default:
	System.out.println( "The number is not 1 or 2" );
	break;
	}

}
}
